fruit = input("I have an apple, an orange and a banana! " \
    + "Which fruit would you like to have? : ")

if fruit == "apple":
    print("Here, have an apple!")

elif fruit == "orange":
    print("Here, have an orange!")

elif fruit == "banana":
    print("Here, have a banana!")

else:
    print("Oops, I don't think I have that.")